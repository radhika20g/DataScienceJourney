import math

x = 5.6
print("ceil:", math.ceil(x))
print("floor:", math.floor(x))
print("truncate:", math.trunc(x))
print('-'*30)

x = 2
print("exponential:", math.exp(x))
print("log:", math.log10(x))
print('-'*30)

print("pi:", math.pi)
print("e:", math.e)
print("factorial of 3:", math.factorial(3))
print('-'*30)

x = 90
print("sin90:", math.sin(x))
print("cos90:", math.cos(x))
print("tan90:", math.tan(x))
print('-'*30)


# integer
print(10)
print(type(10))
print('**************************')

#float
print(10.1)
print(type(10.1))
print('**************************')

#string
print('single quotes')
print("double quotes")
print(type('single'))
print('**************************')

#bool
print(True)
print(type(False))
print('**************************')

#print functionalities
#by default - prints in new line
print('a')
print('bc')
print('d')
print('**************************')

#end - ends the print statement with what is given rather than new line
print('a', end = '_')
print('bc', end = '_')
print('d', end = '_')
print('**************************')

#print in same line, here comma is a seperator
print('a', 'b', 'c')
print('**************************')

#seperator - seperates the lines with what is given rather than space
print('a', 'b', 'c', sep = '*')
print('**************************')
